package p3009;

import java.util.Scanner;

/**
 * 백준 3009번 - 네 번째 점
 * https://www.acmicpc.net/problem/3009
 * 
 * 난이도: Bronze III
 * 분류: 수학, 기하학, 구현
 * 
 * 프로그래밍 기초 개념:
 * 1. 좌표와 점의 개념
 * 2. 직사각형의 성질 이해
 * 3. 중복 검사와 예외 찾기
 * 4. XOR 연산 활용 (고급 방법)
 * 
 * 문제 이해:
 * - 직사각형의 세 점이 주어졌을 때, 네 번째 점을 찾는 문제
 * - 직사각형에서는 x좌표와 y좌표가 각각 두 번씩 나타남
 * - 한 번만 나타나는 좌표들을 찾으면 네 번째 점이 됨
 * 
 * 초보자를 위한 팁:
 * - 직사각형의 특성: 마주보는 두 점의 x좌표 또는 y좌표가 같음
 * - 세 점이 주어지면, 각 좌표값 중 한 번만 나오는 것을 찾으면 됨
 * - 그림을 그려서 이해해보세요!
 */
public class Main {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        
        // 세 점의 좌표를 입력받습니다
        int x1 = sc.nextInt();
        int y1 = sc.nextInt();
        
        int x2 = sc.nextInt(); 
        int y2 = sc.nextInt();
        
        int x3 = sc.nextInt();
        int y3 = sc.nextInt();
        
        // 네 번째 점의 x좌표 찾기
        // 직사각형에서는 x좌표가 두 번씩 나타나므로,
        // 한 번만 나타나는 x좌표가 네 번째 점의 x좌표입니다
        int x4;
        if (x1 == x2) {        // x1과 x2가 같다면
            x4 = x3;           // x3이 네 번째 점의 x좌표
        } else if (x1 == x3) { // x1과 x3이 같다면  
            x4 = x2;           // x2가 네 번째 점의 x좌표
        } else {               // x2와 x3이 같다면
            x4 = x1;           // x1이 네 번째 점의 x좌표
        }
        
        // 네 번째 점의 y좌표 찾기 (같은 원리)
        int y4;
        if (y1 == y2) {        // y1과 y2가 같다면
            y4 = y3;           // y3이 네 번째 점의 y좌표
        } else if (y1 == y3) { // y1과 y3이 같다면
            y4 = y2;           // y2가 네 번째 점의 y좌표  
        } else {               // y2와 y3이 같다면
            y4 = y1;           // y1이 네 번째 점의 y좌표
        }
        
        // 네 번째 점 출력
        System.out.println(x4 + " " + y4);
        
        sc.close();
    }
}

/*
 * 고급 방법 - XOR 연산 활용:
 * 
 * XOR의 성질: A ⊕ A = 0, A ⊕ 0 = A
 * 같은 값끼리 XOR하면 0이 되고, 0과 XOR하면 원래 값이 됩니다.
 * 
 * int x4 = x1 ^ x2 ^ x3;  // 중복된 x좌표는 상쇄되고 유일한 값만 남음
 * int y4 = y1 ^ y2 ^ y3;  // 중복된 y좌표는 상쇄되고 유일한 값만 남음
 * 
 * 이 방법이 더 간결하지만, 초보자에게는 위의 if-else 방법이 이해하기 쉽습니다.
 */

