package p1193;

import java.util.Scanner;

/**
 * 제목: 분수찾기
 * 링크: https://www.acmicpc.net/problem/1193
 * 분류: 수학, 구현
 * 
 * 핵심 아이디어:
 * - 분수를 지그재그 순서로 나열했을 때 N번째 분수를 찾는 문제
 * - 패턴: 1/1 → 1/2, 2/1 → 3/1, 2/2, 1/3 → 1/4, 2/3, 3/2, 4/1 → ...
 * - 각 대각선의 길이: 1, 2, 3, 4, ... (n번째 대각선은 n개의 분수)
 * 
 * 알고리즘 접근:
 * 1. N이 몇 번째 대각선에 속하는지 찾기
 * 2. 대각선 내에서의 위치 계산
 * 3. 홀수 번째 대각선: 분자는 감소, 분모는 증가
 * 4. 짝수 번째 대각선: 분자는 증가, 분모는 감소
 * 
 * 학습 개념:
 * - 수열의 패턴 인식과 규칙 찾기
 * - 누적합을 이용한 구간 판정
 * - 홀수/짝수에 따른 조건 분기
 * - 분수의 분자/분모 계산 규칙
 */
public class Main {
	public static void main(String[] args) {
		int n = new Scanner(System.in).nextInt();  // 찾고자 하는 분수의 순서
		int i = 1;  // 현재 대각선 번호
		
		// N이 속한 대각선 찾기
		while (true) {
			n = n - i;  // 현재 대각선의 분수 개수만큼 차감
			
			if (n <= 0) {  // N이 현재 대각선에 속함
				if (i % 2 == 1) {  // 홀수 번째 대각선 (위에서 아래로)
					// 분자는 1부터 시작해서 증가, 분모는 i부터 시작해서 감소
					System.out.println((1 - n) + "/" + (i + n));
				} else {  // 짝수 번째 대각선 (아래에서 위로)
					// 분자는 i부터 시작해서 감소, 분모는 1부터 시작해서 증가
					System.out.println((i + n) + "/" + (1 - n));
				}
				break;
			}
			i++;  // 다음 대각선으로 이동
		}
	}
}